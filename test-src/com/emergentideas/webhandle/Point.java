package com.emergentideas.webhandle;

public class Point {

	public Object xValue;
	public Object yValue;
	public Object getxValue() {
		return xValue;
	}
	public void setxValue(Object xValue) {
		this.xValue = xValue;
	}
	public Object getyValue() {
		return yValue;
	}
	public void setyValue(Object yValue) {
		this.yValue = yValue;
	}
	
	
}
